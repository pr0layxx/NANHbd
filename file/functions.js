function countdown(date){
    var targetDate = new Date(2024, 8, 7); // September is the 9th month in JavaScript, so we use 8
    var current = new Date();
    var seconds = Math.floor((targetDate.getTime() - current.getTime()) / 1000);
    if(seconds < 0) {
        $("#clock").html("TIME'S UP!");
        return;
    }
    var days = Math.floor(seconds / (3600 * 24));
    seconds = seconds % (3600 * 24);
    var hours = Math.floor(seconds / 3600);
    if (hours < 10) {
        hours = "0" + hours;
    }
    seconds = seconds % 3600;
    var minutes = Math.floor(seconds / 60);
    if (minutes < 10) {
        minutes = "0" + minutes;
    }
    seconds = seconds % 60;
    seconds = Math.floor(seconds); // remove decimal part
    if (seconds < 10) {
        seconds = "0" + seconds;
    }
    var result = "Days <span class=\"digit\">" + days + "</span> Hours <span class=\"digit\">" + hours + "</span> Minutes <span class=\"digit\">" + minutes + "</span> Seconds <span class=\"digit\">" + seconds; 
    $("#clock").html(result);

    var text = "COUNTDOWN TO 7 SEPTEMBER 2024";
    $("#message-box").html(text);
}

setInterval(countdown, 1000); // update every second